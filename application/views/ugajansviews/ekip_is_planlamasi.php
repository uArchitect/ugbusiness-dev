// Advanced Drag and Drop System
let dragState = {
 isDragging: false,
 draggedElement: null,
 draggedEvent: null,
 dragGhost: null,
 dropTarget: null,
 originalPosition: null,
 listenersAttached: false
};

function handleEventDragStart(e) {
 const eventBlock = e.target.closest('.event-block');
 if (!eventBlock) {
  e.preventDefault();
  return;
 }
 
 e.stopPropagation();
 
 // Validate event block data
 const eventId = eventBlock.dataset.eventId;
 const personId = eventBlock.dataset.personId;
 const date = eventBlock.dataset.date;
 
 if (!eventId || !personId || !date) {
  console.error('Invalid event block data:', {
   eventId: eventId,
   personId: personId,
   date: date
  });
  e.preventDefault();
  return;
 }
 
 dragState.isDragging = true;
 dragState.draggedElement = eventBlock;
 dragState.draggedEvent = {
  id: String(eventId),
  personId: String(personId),
  date: String(date),
  startTime: eventBlock.dataset.startTime || '09:00',
  endTime: eventBlock.dataset.endTime || '17:00'
 };
 
 // Store original position
 const rect = eventBlock.getBoundingClientRect();
 dragState.originalPosition = {
  top: eventBlock.style.top,
  height: eventBlock.style.height,
  left: eventBlock.style.left,
  width: eventBlock.style.width
 };
 
 // Create drag ghost
 createDragGhost(eventBlock, e);
 
 // Visual feedback
 eventBlock.classList.add('dragging');
 eventBlock.style.opacity = '0.4';
 
 // Set drag data
 e.dataTransfer.effectAllowed = 'move';
 e.dataTransfer.setData('text/plain', dragState.draggedEvent.id);
 
 // Add global drag listeners (only once)
 if (!dragState.listenersAttached) {
  document.addEventListener('dragover', handleGlobalDragOver);
  document.addEventListener('dragleave', handleGlobalDragLeave);
  document.addEventListener('drop', handleGlobalDrop);
  dragState.listenersAttached = true;
 }
}

function handleEventDragEnd(e) {
 if (!dragState.isDragging) return;
 
 // Remove drag ghost
 if (dragState.dragGhost) {
  dragState.dragGhost.remove();
  dragState.dragGhost = null;
 }
 
 // Restore original element if drag was cancelled
 if (dragState.draggedElement) {
  dragState.draggedElement.classList.remove('dragging');
