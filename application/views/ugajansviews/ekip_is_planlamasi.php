function checkConflict(personId, date, excludeEventId, callback, startTime = null, endTime = null) {
	// Validate parameters
	if (!personId || !date) {
		console.error('Invalid parameters for checkConflict:', { personId, date, excludeEventId });
		if (callback) callback(false, 'Geçersiz parametreler');
		return;
	}
 
	// Ensure excludeEventId is a string
	const safeExcludeEventId = excludeEventId ? String(excludeEventId) : '';
 
	// Build request body
	let body = 'person_id=' + encodeURIComponent(String(personId)) + 
		'&date=' + encodeURIComponent(String(date)) + 
		'&exclude_event_id=' + encodeURIComponent(safeExcludeEventId);
	
	// Add time fields if provided
	if (startTime && endTime) {
		body += '&start_time=' + encodeURIComponent(String(startTime)) + 
			'&end_time=' + encodeURIComponent(String(endTime));
	}
	
	fetch('<?=base_url("ugajans_ekip/ajax_check_conflict")?>', {
		method: 'POST',
		headers: {
			'Content-Type': 'application/x-www-form-urlencoded',
			'X-Requested-With': 'XMLHttpRequest'
		},
		body: body
	})
 .then(response => {
  if (!response.ok) {
   throw new Error('HTTP error! status: ' + response.status);
  }
  return response.json();
 })
 .then(data => {
  // Validate callback exists and data is valid
  if (!callback) {
   console.warn('checkConflict callback is null');
   return;
  }
  
  // Ensure data is valid
  if (!data || typeof data !== 'object') {
   console.error('Invalid response data in checkConflict:', data);
   callback(false, 'Geçersiz sunucu yanıtı');
   return;
  }
  
  // Safely call callback
  try {
   callback(data.has_conflict || false, data.message || '');
  } catch (callbackError) {
   console.error('Error in checkConflict callback:', callbackError);
  }
 })
 .catch(error => {
  console.error('Error in checkConflict:', error);
  // Safely call callback even on error
  if (callback) {
   try {
    callback(false, 'Bağlantı hatası: ' + (error.message || 'Bilinmeyen hata'));
   } catch (callbackError) {
    console.error('Error in checkConflict error callback:', callbackError);
   }
  }
 });
}

