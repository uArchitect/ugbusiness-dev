function handleGlobalDrop(e) {
 e.preventDefault();
 e.stopPropagation();
 
 if (!dragState.isDragging || !dragState.draggedEvent || !dragState.draggedEvent.id) {
  console.warn('Invalid drag state on drop:', dragState);
  handleEventDragEnd(e);
  return;
 }
 
 const dropTarget = findDropTarget(e);
 
 if (!dropTarget) {
  console.warn('No drop target found, cancelling drag');
  // Restore element to original position
  if (dragState.draggedElement) {
   dragState.draggedElement.style.opacity = '1';
   dragState.draggedElement.classList.remove('updating', 'dragging');
  }
  handleEventDragEnd(e);
  return;
 }
 
 // Get person ID and date from drop target - use getAttribute for reliability
 const newPersonId = dropTarget.getAttribute('data-person-id');
 const newDate = dropTarget.getAttribute('data-date');
 
 // Validate drop target data - check for null/undefined/empty string explicitly
 // This ensures person_id "1" is not treated as falsy
 if (newPersonId === null || newPersonId === undefined || newPersonId === '' ||
     newDate === null || newDate === undefined || newDate === '') {
  console.error('Invalid drop target data:', {
   personId: newPersonId,
   date: newDate,
   dropTarget: dropTarget
  });
  dropTarget.classList.remove('drag-over');
  if (dragState.draggedElement) {
   dragState.draggedElement.style.opacity = '1';
   dragState.draggedElement.classList.remove('updating', 'dragging');
  }
  handleEventDragEnd(e);
  return;
 }
 
 // Ensure values are strings
 const validatedPersonId = String(newPersonId).trim();
 const validatedDate = String(newDate).trim();
 
 // Validate after trimming - check for empty string explicitly
 if (validatedPersonId === '' || validatedDate === '') {
  console.error('Empty drop target data after validation');
  dropTarget.classList.remove('drag-over');
  handleEventDragEnd(e);
  return;
 }
 
 console.log('Drop target validated:', {
  personId: validatedPersonId,
  date: validatedDate,
  originalPersonId: dragState.draggedEvent?.personId,
  originalDate: dragState.draggedEvent?.date
 });
 
 // Check if dropped on same position
 if (String(dragState.draggedEvent.personId) === validatedPersonId && 
     String(dragState.draggedEvent.date) === validatedDate) {
  console.log('Dropped on same position, cancelling');
  dropTarget.classList.remove('drag-over');
  handleEventDragEnd(e);
  return;
 }
 
 // Show loading state
 if (dragState.draggedElement) {
  dragState.draggedElement.style.opacity = '0.3';
  dragState.draggedElement.classList.add('updating');
 }
 
 // Validate drag state and capture values immediately (before async operations)
 if (!dragState.draggedEvent || !dragState.draggedEvent.id) {
  console.error('Invalid drag state:', dragState);
  dropTarget.classList.remove('drag-over');
  handleEventDragEnd(e);
  return;
 }
 
	// Capture all needed values BEFORE async operations to prevent state changes
	const capturedEventId = String(dragState.draggedEvent.id);
	const capturedPersonId = String(dragState.draggedEvent.personId);
	const capturedDate = String(dragState.draggedEvent.date);
	const capturedElement = dragState.draggedElement;
	const capturedOriginalPosition = dragState.originalPosition;
 
	// Check for conflicts first - include time information if available
	const capturedStartTime = capturedElement.getAttribute('data-start-time') || 
	                          capturedElement.dataset.startTime || 
	                          dragState.draggedEvent?.startTime || 
	                          null;
	const capturedEndTime = capturedElement.getAttribute('data-end-time') || 
	                        capturedElement.dataset.endTime || 
	                        dragState.draggedEvent?.endTime || 
	                        null;
	
	// Wrap callback in try-catch for safety
	// Use validated values - pass time information for better conflict checking
	checkConflict(validatedPersonId, validatedDate, capturedEventId, function(hasConflict, message) {
  try {
   // Re-validate state in callback (state might have changed)
   if (!capturedEventId) {
    console.warn('Event ID is missing in conflict check callback');
    if (dropTarget) dropTarget.classList.remove('drag-over');
    handleEventDragEnd(e);
    return;
   }
   
   // Check if drag is still active (optional check, but safer)
   if (!dragState.isDragging) {
    console.warn('Drag state changed during conflict check - drag no longer active');
    // Don't return here, still process the result
   }
   
   if (hasConflict) {
    // Show error message
    showNotification('Çakışma: ' + (message || 'Zaman çakışması tespit edildi'), 'error');
    if (dropTarget) dropTarget.classList.remove('drag-over');
    
    // Restore element
    if (capturedElement) {
     capturedElement.style.opacity = '1';
     capturedElement.classList.remove('updating');
    }
    
    handleEventDragEnd(e);
    return;
   }
   
   // Optimistic UI update - move element immediately
   if (capturedElement && dropTarget) {
    moveEventVisually(capturedElement, dropTarget, validatedPersonId, validatedDate);
   }
   
   // Update via AJAX - use validated values
   updateEventPosition(capturedEventId, validatedPersonId, validatedDate, function(success, errorMessage) {
    try {
     // Re-validate state in callback
     if (!capturedEventId) {
      console.warn('Event ID lost during update');
      return;
     }
     
     if (success) {
      // Success - refresh calendar data
      refreshCalendarData(function() {
       showNotification('Görev başarıyla taşındı', 'success');
       initCalendar();
      });
     } else {
      // Failure - revert visual change
      if (capturedElement && capturedOriginalPosition) {
       revertEventPositionWithData(capturedElement, capturedPersonId, capturedDate, capturedOriginalPosition);
      }
      showNotification(errorMessage || 'Güncelleme başarısız oldu', 'error');
     }
     
     if (dropTarget) dropTarget.classList.remove('drag-over');
     handleEventDragEnd(e);
    } catch (updateError) {
     console.error('Error in updateEventPosition callback:', updateError);
     if (dropTarget) dropTarget.classList.remove('drag-over');
     handleEventDragEnd(e);
    }
   });
  } catch (conflictError) {
   console.error('Error in checkConflict callback:', conflictError);
   if (dropTarget) dropTarget.classList.remove('drag-over');
   handleEventDragEnd(e);
  }
 }, capturedStartTime, capturedEndTime);
}

function findDropTarget(e) {
 if (!e || !e.target) {
  console.warn('findDropTarget: Invalid event or target');
  return null;
 }
 
 // If dragging over an event-block, find the underlying calendar-day-cell
 // by getting the event-block's parent time-slots container, then its parent calendar-day-cell
 const eventBlock = e.target.closest('.event-block');
 if (eventBlock) {
  // Find the time-slots container that contains this event
  const timeSlots = eventBlock.closest('.time-slots');
  if (timeSlots) {
   const dayCell = timeSlots.closest('.calendar-day-cell');
   if (dayCell && dayCell.hasAttribute('data-drop-zone')) {
    const personId = dayCell.getAttribute('data-person-id');
    const date = dayCell.getAttribute('data-date');
    // Check for null/undefined/empty string explicitly (not just falsy)
    if (personId !== null && personId !== undefined && personId !== '' && 
        date !== null && date !== undefined && date !== '') {
     console.log('Found drop target via event-block:', { personId, date });
     return dayCell;
    }
   }
  }
 }
 
 // Try to find calendar-day-cell directly using closest (more reliable)
 const dayCell = e.target.closest('.calendar-day-cell');
 if (dayCell && dayCell.hasAttribute('data-drop-zone')) {
  const personId = dayCell.getAttribute('data-person-id');
  const date = dayCell.getAttribute('data-date');
  // Check for null/undefined/empty string explicitly (not just falsy)
  if (personId !== null && personId !== undefined && personId !== '' && 
      date !== null && date !== undefined && date !== '') {
   const dropZone = dayCell.getAttribute('data-drop-zone');
   if (dropZone === 'true') {
    console.log('Found valid drop target:', { personId, date, element: dayCell });
    return dayCell;
   }
  }
 }
 
 // Fallback: try to find time-slot and get its parent calendar-day-cell
 const timeSlot = e.target.closest('.time-slot');
 if (timeSlot) {
  const dayCell = timeSlot.closest('.calendar-day-cell');
  if (dayCell && dayCell.hasAttribute('data-drop-zone')) {
   const personId = dayCell.getAttribute('data-person-id');
   const date = dayCell.getAttribute('data-date');
   // Check for null/undefined/empty string explicitly (not just falsy)
   if (personId !== null && personId !== undefined && personId !== '' && 
       date !== null && date !== undefined && date !== '') {
    console.log('Found drop target via time-slot:', { personId, date });
    return dayCell;
   }
  }
 }
 
 console.warn('No valid drop target found');
 return null;
}

function moveEventVisually(element, targetCell, newPersonId, newDate) {
 if (!element || !targetCell) {
  console.error('Invalid parameters for moveEventVisually:', { element, targetCell });
  return;
 }
 
 // Validate parameters
 const validatedPersonId = String(newPersonId).trim();
 const validatedDate = String(newDate).trim();
 
 if (!validatedPersonId || !validatedDate) {
  console.error('Invalid person ID or date in moveEventVisually:', { newPersonId, newDate });
  return;
 }
 
 // Verify target cell has correct attributes
 const targetPersonId = targetCell.getAttribute('data-person-id');
 const targetDate = targetCell.getAttribute('data-date');
 
 if (targetPersonId !== validatedPersonId || targetDate !== validatedDate) {
  console.error('Target cell mismatch:', {
   expected: { personId: validatedPersonId, date: validatedDate },
   actual: { personId: targetPersonId, date: targetDate }
  });
  return;
 }
 
 // Get start/end time from element's data attributes (more reliable)
 const startTime = element.getAttribute('data-start-time') || 
                  element.dataset.startTime || 
                  dragState.draggedEvent?.startTime || 
                  '09:00';
 const endTime = element.getAttribute('data-end-time') || 
                element.dataset.endTime || 
                dragState.draggedEvent?.endTime || 
                '17:00';
 
