<?php
// Başlangıçta var olan trjsconfig dizisi
$trjsconfig = array(
    "general" => array(
        "borderColor" => "#9CA8B6",
        "visibleNames" => "#ffffff"
    )
);

$sehirler = get_sehirler();
foreach ($sehirler as $sehir) {
  $color = "";



  if($sehir->kullanici_ad_soyad != ""){
$rut_baslangic_tarihi = strtotime($sehir->rut_baslangic_tarihi);   
$rut_bitis_tarihi = strtotime($sehir->rut_bitis_tarihi);  
$simdi = strtotime(date("Y-m-d 00:00"));  

 
if ($simdi < $rut_baslangic_tarihi) {
  $color = "#ffa000db";
} elseif ($simdi >= $rut_baslangic_tarihi && $simdi <= $rut_bitis_tarihi) {
  $color = "#055505";
} else {
  $color = "#cd0000db";
}
}else{
  $color = "#4f4f4f8f";
 }


 
 
$newObject = array(
  "hover" => $sehir->sehir_adi,
  "url" => base_url("rut/form/".$sehir->sehir_id),
  "target" => "same_window",
  "upColor" => $color,
  "overColor" => "#3535358f",
  "downColor" => "#0c0c0c",
  "active" => true
);
$trjsconfig[$sehir->map_kodu] = $newObject;



}



 
// trjsconfig dizisini tekrar JavaScript nesnesine dönüştürme
$js_trjsconfig = json_encode($trjsconfig);
?>

